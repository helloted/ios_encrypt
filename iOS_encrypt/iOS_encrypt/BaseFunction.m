+ (long)getAppNumberVersion
{
    long numberVersion = 0;
    NSDictionary* infoDict =[[NSBundle mainBundle] infoDictionary];
    NSString* stringVersion =[infoDict objectForKey:@"CFBundleShortVersionString"];
    if (stringVersion)
    {
        NSArray *versionArray = [stringVersion componentsSeparatedByString:@"."];
        if ([versionArray count] >= 3)
        {
            long highNum = [[versionArray objectAtIndex:0] integerValue];
            long midNum = [[versionArray objectAtIndex:1] integerValue];
            long lowNum = [[versionArray objectAtIndex:2] integerValue];
            numberVersion = highNum * 256 * 256 + midNum * 256 + lowNum * 1;
        }
    }
    return numberVersion;
}

