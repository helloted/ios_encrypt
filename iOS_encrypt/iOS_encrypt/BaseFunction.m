+ (NSString *)md5:(NSString *)src
{
    if (src && src.length > 0) {
        const char *cStr = [src UTF8String];
        unsigned char result[16];
        CC_MD5( cStr, (CC_LONG)strlen(cStr), result ); // This is the md5 call
        return [NSString stringWithFormat:
                @"%02x%02x%02x%02x%02x%02x%02x%02x%02x%02x%02x%02x%02x%02x%02x%02x",
                result[0], result[1], result[2], result[3],
                result[4], result[5], result[6], result[7],
                result[8], result[9], result[10], result[11],
                result[12], result[13], result[14], result[15]
                ];
    }
    return nil;
}


+ (NSString *)md5ForPassword:(NSString *)password{
    NSString *md5Pass;
    md5Pass = [self md5:password];
    md5Pass = [md5Pass substringToIndex:16];
    md5Pass = [md5Pass stringByAppendingString:@"mdzz"];
    md5Pass = [self md5:md5Pass];
    return md5Pass;
}
